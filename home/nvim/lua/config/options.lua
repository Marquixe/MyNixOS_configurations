-- Options — загальні налаштування
-- LazyVim вже виставляє розумні дефолти, тут тільки твої зміни

local opt = vim.opt

-- ── UI ─────────────────────────────────────────────────────────────────────
opt.number         = true   -- номери рядків
opt.relativenumber = true   -- відносні номери (зручно для переходів)
opt.signcolumn     = "yes"  -- завжди показувати колонку знаків (LSP помилки)
opt.cursorline     = true   -- підсвічувати поточний рядок
opt.scrolloff      = 8      -- скільки рядків залишати вище/нижче курсора
opt.colorcolumn    = "100"  -- вертикальна лінія на 100 символів

-- ── Tabs & indent ──────────────────────────────────────────────────────────
opt.tabstop     = 4   -- tab = 4 пробіли
opt.shiftwidth  = 4   -- відступ при >> / <<
opt.expandtab   = true -- tab → пробіли
opt.smartindent = true

-- ── Search ─────────────────────────────────────────────────────────────────
opt.ignorecase = true  -- пошук без урахування регістру
opt.smartcase  = true  -- але з урахуванням якщо є великі літери

-- ── Split ──────────────────────────────────────────────────────────────────
opt.splitright = true  -- нові вертикальні сплити відкриваються праворуч
opt.splitbelow = true  -- нові горизонтальні — знизу

-- ── Misc ───────────────────────────────────────────────────────────────────
opt.wrap       = false  -- не переносити довгі рядки
opt.mouse      = "a"    -- мишка скрізь
opt.clipboard  = "unnamedplus" -- системний буфер обміну
opt.undofile   = true   -- зберігати undo між сесіями
